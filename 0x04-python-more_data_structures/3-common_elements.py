"""
    ------------
    About Module
    ------------
    returns a set of common elements in two sets.

    ---------
    More Info
    ---------
    @set1: first set
    @set2: second set
"""


def common_elements(set_1, set_2):

    """ Return the intersection of the two sets """

    return (set_1 & set_2)
